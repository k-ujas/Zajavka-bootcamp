package zajavka.Day7;

public class ComplicatedOperators {
    public static void main(String[] args) {
        int a = 3;
        a += 2; // a = a + 2
        System.out.println("a: " + a);

        int b = 9;
        b -= 1; // b = b - 1
        System.out.println("b: " + b);

        int c = 10;
        c *= 2; // c = c * 2
        System.out.println("c: " + c);

        int d = 20;
        d /= 5; // d = d / 5
        System.out.println("d: " + d);

        int e = 11;
        e %= 10; // e = e % 10
        System.out.println("e: " + e);
    }
}
