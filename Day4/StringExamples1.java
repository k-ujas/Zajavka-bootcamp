package zajavka.Day4;

public class StringExamples {
    public static void main(String[] args) {
        String s1 = "Hello World!";
        System.out.println(s1.charAt(6)); // show W
        System.out.println(s1.contains("World")); // is word "World" in s1? = true
        System.out.println(s1.substring(6, 12)); // show World!
        System.out.println(s1);
        System.out.println(s1);
        System.out.println(s1);
    }
}
